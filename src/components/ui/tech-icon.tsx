// Fallback monogram icon
export function MonogramIcon({
  name,
  color,
  size = 24,
  className,
}: {
  name: string;
  color: string;
  size?: number;
  className?: string;
}) {
  const initials = name
    .replace(/[^a-zA-Z\s]/g, "")
    .split(/\s+/)
    .map((w) => w[0])
    .join("")
    .slice(0, 2)
    .toUpperCase();
  return (
    <svg viewBox="0 0 24 24" width={size} height={size} className={className} aria-hidden>
      <rect width="24" height="24" rx="4" fill={color} fillOpacity="0.15" />
      <text
        x="12"
        y="16"
        textAnchor="middle"
        fontSize="9"
        fontWeight="700"
        fontFamily="ui-monospace,monospace"
        fill={color}
      >
        {initials}
      </text>
    </svg>
  );
}

